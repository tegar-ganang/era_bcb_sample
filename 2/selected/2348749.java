package CURED;

import javax.net.ssl.HttpsURLConnection;
import javax.net.ssl.SSLContext;
import java.net.URL;
import java.io.*;
import javax.net.ssl.TrustManager;

/**
 * The purpose of this class is to retrieve HTML code from a web page.
 * @author Travis McGahey
 */
public class Wget {

    /** Creates a new instance of wget */
    public Wget() {
    }

    /**
     * Send POST data to an SSL encrypted web page and return the HTML results.
     * NOTE:  Currently the web certificate will automatically be accepted regardless of 
     * what organization created the certificate.
     * @param url URL of the web page to connect to.<BR>
     * eg. https://www.something.com/index.php<BR>
     * @param query POST data to send to URL.<BR>
     * eg. name=sam&pass=in<BR>
     * @return HTML code generated by the specified web page.<BR>
     * @throws java.lang.Exception Fix: this should be a specific exception.  Right now just a general exception.<BR>
     */
    public String getURLWithPost(String url, String query) throws Exception {
        TrustManager[] trustAllCerts = new TrustManager[] { new TrustAllManager() };
        SSLContext sc = SSLContext.getInstance("SSL");
        sc.init(null, trustAllCerts, new java.security.SecureRandom());
        HttpsURLConnection.setDefaultSSLSocketFactory(sc.getSocketFactory());
        HttpsURLConnection httpConn = (HttpsURLConnection) new URL(url).openConnection();
        httpConn.setRequestMethod("POST");
        httpConn.setDoOutput(true);
        httpConn.connect();
        OutputStream out = httpConn.getOutputStream();
        out.write(query.getBytes());
        InputStreamReader isr = new InputStreamReader(httpConn.getInputStream());
        BufferedReader in = new BufferedReader(isr);
        String inputLine;
        String allLines = "";
        while ((inputLine = in.readLine()) != null) {
            allLines += inputLine;
        }
        out.close();
        in.close();
        return allLines;
    }
}
